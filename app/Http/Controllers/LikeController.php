     * Store a newly created resource in storage.
     */
    public function store(StoreLikeRequest $request)
    {
        $like = Like::where('id_foto', request('id_foto'))->where('id_user', auth()->id())->first();
        if ($like) {
            $like->delete();
            return back();
        }

        $data = [
            'id_foto'=>request('id_foto'),
            'id_user'=>auth()->id()
        ];

        Like::create($data);
        return back();
    }

